-- TABLE
CREATE TABLE "APPARTEMENT" (
  ID_APPART INT(10) NOT NULL,
  ETAGE INT(2),
  NB_PIECE int(2),
  SUPERFICIE INT(3),
  ID_IMMEUBLE int(10) NOT NULL, NB_FENETRE int(2),
  PRIMARY KEY (ID_APPART),
  FOREIGN KEY (ID_IMMEUBLE) REFERENCES IMMEUBLE
 );
CREATE TABLE AUTEUR (
  ID_AUTEUR int(5) NOT NULL,
  NOM VARCHAR(40),
  PRENOM VARCHAR(40),
  DDN int(4),
  NATIONAL VARCHAR(40),
  PRIMARY KEY (ID_AUTEUR)
);
CREATE TABLE IMMEUBLE (
  ID_IMMEUBLE INT(10) NOT NULL,
  NB_ETAGE INT(2),
  ADRESSE varchar(255),
  ASCENSEUR BOOL,
  DATECREA date, CODE_ENTREE int(4), HAUTEUR int(5),
  PRIMARY KEY (ID_IMMEUBLE)
 );
CREATE TABLE LIVRE (
  ID_LIVRE int(6) NOT NULL,
  TITRE VARCHAR(255),
  SORTIE int(4),
  NB_PAGES int(4),
  DESCR VARCHAR(255),
  ID_AUTEUR int(5) NOT NULL,
  PRIMARY KEY (ID_LIVRE),
  FOREIGN KEY (ID_AUTEUR) REFERENCES AUTEUR
);
 
